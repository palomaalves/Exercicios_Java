package projeto.vendas;
public class Contatos {
	private int tel1;
	private int tel2;
	private int cel1;
	private int cel2;
	private String email;
	
	public int getTel1() {
		return tel1;
	}
	
	public void setTel1(int tel1) {
		this.tel1 = tel1;
	}
	
	public int getTel2() {
		return tel2;
	}
	
	public void setTel2(int tel2) {
		this.tel2 = tel2;
	}
	
	public int getCel1() {
		return cel1;
	}
	
	public void setCel1(int cel1) {
		this.cel1 = cel1;
	}
	
	public int getCel2() {
		return cel2;
	}
	
	public void setCel2(int cel2) {
		this.cel2 = cel2;
	}
	
	public String getEmail() {
		return email;
	}
	
	public void setEmail(String email) {
		this.email = email;
	}
}
