package projeto.vendas;
public class Compra {
	private int codCompra;
	private String notaFiscal;
	private int codProCompra;
	private int qtdCompra;
	private int codForCompra;
	private String dataCompra;
	
	public int getCodCompra() {
		return codCompra;
	}
	
	public void setCodCompra(int codCompra) {
		this.codCompra = codCompra;
	}
	
	public String getNotaFiscal() {
		return notaFiscal;
	}
	
	public void setNotaFiscal(String notaFiscal) {
		this.notaFiscal = notaFiscal;
	}
	
	public int getCodProCompra() {
		return codProCompra;
	}
	
	public void setCodProCompra(int codProCompra) {
		this.codProCompra = codProCompra;
	}
	
	public int getQtdCompra() {
		return qtdCompra;
	}
	
	public void setQtdCompra(int qtdCompra) {
		this.qtdCompra = qtdCompra;
	}
	
	public int getCodForCompra() {
		return codForCompra;
	}
	
	public void setCodForCompra(int codForCompra) {
		this.codForCompra = codForCompra;
	}
	
	public String getDataCompra() {
		return dataCompra;
	}
	
	public void setDataCompra(String dataCompra) {
		this.dataCompra = dataCompra;
	}
	
	
}
